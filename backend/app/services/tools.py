def _gen_project_name(raw: str) -> str:
    q = raw.strip()
    for prefix in ("auto file write", "create project:", "build project:"):
        if q.lower().startswith(prefix):
            q = q[len(prefix):].strip()
            break
    for kw in ("react", "fastapi", "vue", "django", "flask", "nodejs", "node.js",
               "nextjs", "next.js", "typescript", "javascript", "with", r"\+"):
        q = re.sub(r"(?i)\b" + re.escape(kw.lstrip(r"\\")) + r"\b", "", q)
    q = re.sub(r"\s+", "-", q.strip()).strip("-")
    q = re.sub(r"-+", "-", re.sub(r"[^\w\-]", "", q))
    return q.lower() or "my-project"


