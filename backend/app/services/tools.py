def safe_path(path: str) -> Path:
    clean = path.strip().strip('"').strip("'")
    target = (PROJECT_ROOT / clean).resolve()
    if not str(target).startswith(str(PROJECT_ROOT)):
        raise PermissionError("Access denied.")
    return target


def run_python(code: str) -> str:
    try:
        r = subprocess.run(
            ["python", "-c", code],
            capture_output=True,
            text=True,
            timeout=5,
        )
        return r.stdout or r.stderr or "No output"
    except Exception as e:
        return str(e)


_ALLOWED_OPS = {
    ast.Add: operator.add,
    ast.Sub: operator.sub,
    ast.Mult: operator.mul,
    ast.Div: operator.truediv,
    ast.Pow: operator.pow,
    ast.USub: operator.neg,
}


def _eval_math(node):
    if isinstance(node, ast.Constant):
        return node.value
    if isinstance(node, ast.BinOp):
        return _ALLOWED_OPS[type(node.op)](
            _eval_math(node.left),
            _eval_math(node.right),
        )
    if isinstance(node, ast.UnaryOp):
        return _ALLOWED_OPS[type(node.op)](_eval_math(node.operand))
    raise ValueError("Invalid expression")


def calculator(expr: str) -> str:
    try:
        tree = ast.parse(expr, mode="eval")
        return str(_eval_math(tree.body))
    except Exception as e:
        return str(e)


def read_file(path: str) -> str:
    try:
        target = safe_path(path)
        if not target.exists():
            return f"File not found: {path}"
        if target.is_dir():
            return f"{path} is a folder."
        return target.read_text(encoding="utf-8", errors="ignore")
    except Exception as e:
        return str(e)


def write_file(payload: str) -> str:
    try:
        path, content = payload.split("\nCONTENT:\n", 1)
        target = safe_path(path)
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(content, encoding="utf-8")
        return f"File written: {target.relative_to(PROJECT_ROOT)}"
    except Exception as e:
        return str(e)


def append_file(payload: str) -> str:
    try:
        path, content = payload.split("\nCONTENT:\n", 1)
        target = safe_path(path)
        target.parent.mkdir(parents=True, exist_ok=True)
        with target.open("a", encoding="utf-8") as f:
            f.write(content)
        return f"Content appended: {target.relative_to(PROJECT_ROOT)}"
    except Exception as e:
        return str(e)


def _project_name(raw: str) -> str:
    clean = (
        raw.strip()
        .lower()
        .replace("build project:", "")
        .replace("create project:", "")
        .replace("build api:", "")
        .replace("create api:", "")
        .replace("api:", "")
        .replace("fullstack", "")
        .replace("full stack", "")
        .replace("with database", "")
        .replace("advanced", "")
        .strip()
        .replace(" ", "_")
        .replace("-", "_")
    )
    return clean or "new_project"


def _write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


