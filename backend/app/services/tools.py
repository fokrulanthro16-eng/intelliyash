def explain_code(raw: str) -> str:
    code = re.sub(r"(?i)^explain code:\s*", "", raw).strip()
    q = code.lower()
    lines = code.strip().splitlines()

    if re.search(r"\bdef \b|import \w|print\(", code):
        lang = "Python"
    elif re.search(r"\bfunction\b|\bconst\b|\blet\b|\bconsole\.log\b", code):
        lang = "JavaScript/TypeScript"
    elif re.search(r"<[a-z][\w]*", code):
        lang = "HTML/JSX"
    else:
        lang = "Code"

    what = []
    funcs = re.findall(r"def (\w+)\s*\(", code)
    if funcs:
        what.append(f"Defines function(s): {', '.join(funcs)}")
    for cls in re.findall(r"class (\w+)", code):
        what.append(f"Defines class: {cls}")
    if re.search(r"\breturn\b", code):
        what.append("Returns a computed value")
    if re.search(r"\bfor\b|\bwhile\b", code):
        what.append("Iterates with a loop")
    if re.search(r"\bif\b", code):
        what.append("Contains conditional logic")
    imports = re.findall(r"import (\w+)|from (\w+)", code)
    if imports:
        flat = [a or b for a, b in imports]
        what.append(f"Imports: {', '.join(flat[:4])}")
    if not what:
        what.append(f"Block of {len(lines)} line(s) of {lang}")

    tips = []
    if funcs and '"""' not in code and "'''" not in code:
        tips.append("Add docstrings to document what each function does")
    if re.search(r"except\s*:", code):
        tips.append("Replace bare `except:` with specific exception types")
    if len(lines) > 30:
        tips.append("Consider splitting into smaller, focused functions")
    if re.search(r"def \w+\(.*\)\s*:", code) and "->" not in code:
        tips.append("Add type hints for better readability and IDE support")
    if not tips:
        tips.append("Looks clean — consider adding comments for complex logic")

    out = f"🔍 Summary\n{lang} code"
    if funcs:
        out += f" — defines {', '.join(funcs)}"
    out += f" ({len(lines)} line{'s' if len(lines) != 1 else ''})\n"

    out += "\n⚙️ How it works\n"
    for w in what:
        out += f"  • {w}\n"

    out += "\n💡 Suggestions\n"
    for t in tips:
        out += f"  • {t}\n"
    return out


def fix_error(raw: str) -> str:
    err = re.sub(r"(?i)^fix error:\s*", "", raw).strip()
    e = err.lower()

    def _fmt(problem: str, cause: str, fix: str, example: str) -> str:
        return (
            f"❌ Problem\n  {problem}\n\n"
            f"🔎 Cause\n  {cause}\n\n"
            f"✅ Fix\n{fix}\n\n"
            f"🧪 Example\n{example}"
        )

    if "no module named" in e or "modulenotfounderror" in e:
        m = re.search(r"no module named ['\"]?([\w\.]+)['\"]?", err, re.IGNORECASE)
        pkg = m.group(1) if m else "the-package"
        return _fmt(
            f"ModuleNotFoundError: No module named '{pkg}'",
            f"'{pkg}' is not installed in the current Python environment.",
            f"```bash\npip install {pkg}\n```",
            f"```bash\n# activate venv first (if using one)\nvenv\\Scripts\\activate   # Windows\nsource venv/bin/activate # Mac/Linux\npip install {pkg}\n```",
        )

    if "syntaxerror" in e:
        return _fmt(
            "SyntaxError: invalid syntax",
            "Missing `:`, unmatched brackets, or inconsistent indentation.",
            "  1. Find the line number in the traceback\n"
            "  2. Check for missing `:` after `if / for / def / class`\n"
            "  3. Check for unmatched `()`, `[]`, `{}`\n"
            "  4. Use 4 spaces per indent level — no tabs",
            "```python\ndef add(a, b):  # colon required\n    return a + b\n```",
        )

    if "typeerror" in e:
        return _fmt(
            "TypeError: unsupported operand or wrong argument type",
            "A value of the wrong type was passed to a function or operator.",
            "  1. Check variable types with `type(x)`\n"
            "  2. Convert as needed: `str()`, `int()`, `float()`",
            "```python\nresult = int(value) + 1  # convert before adding\n```",
        )

    if "nameerror" in e or "is not defined" in e:
        return _fmt(
            "NameError: name is not defined",
            "Variable or function used before it was defined, or it was misspelled.",
            "  1. Check spelling carefully\n"
            "  2. Define the variable before using it\n"
            "  3. Variables inside functions are local — check scope",
            "```python\nname = 'Alice'  # define first\nprint(name)     # then use\n```",
        )

    if "attributeerror" in e:
        return _fmt(
            "AttributeError: object has no attribute",
            "Calling a method or property that doesn't exist on that type.",
            "  1. `print(type(obj))` to inspect the type\n"
            "  2. `print(dir(obj))` to list valid attributes\n"
            "  3. Check for typos in the attribute name",
            "```python\nprint(type(my_var))  # check what it actually is\nprint(dir(my_var))   # list available attributes\n```",
        )

    if "indentationerror" in e:
        return _fmt(
            "IndentationError: unexpected indent",
            "Mixed tabs and spaces, or wrong indent level.",
            "  Use exactly 4 spaces per level — no tabs.\n"
            "  In VS Code: View → Render Whitespace to spot mixed indent.",
            "```python\ndef greet():\n    print('hello')  # 4 spaces, not a tab\n```",
        )

    if "connection refused" in e or "connectionerror" in e:
        return _fmt(
            "ConnectionRefusedError: connection refused",
            "The backend server is not running, or the port is wrong.",
            "```bash\nuvicorn main:app --reload --port 8000\n```",
            "Confirm the URL matches: http://127.0.0.1:8000",
        )

    if "cors" in e:
        return _fmt(
            "CORS error: request blocked by browser",
            "Frontend origin is not allowed by the backend's CORS policy.",
            "Add this to your FastAPI app:\n"
            "```python\nfrom fastapi.middleware.cors import CORSMiddleware\n\napp.add_middleware(\n    CORSMiddleware,\n    allow_origins=['*'],\n    allow_methods=['*'],\n    allow_headers=['*'],\n)\n```",
            "Place `add_middleware` right after `app = FastAPI()`.",
        )

    return (
        f"❌ Problem\n  {err}\n\n"
        "🔎 Cause\n  Unknown — read the full traceback for details.\n\n"
        "✅ Fix\n"
        "  1. Read the full traceback — the real error is the last line\n"
        "  2. Search the exact message on Stack Overflow\n"
        "  3. Reinstall dependencies: `pip install -r requirements.txt`\n"
        "  4. Check Python version: `python --version`\n\n"
        "🧪 Example\n  Paste the exact traceback for a more specific fix."
    )


